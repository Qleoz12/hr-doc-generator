package cz.ibm.hci;

public class DataBean {
	
	private String laborContractId;
	private String firstName;
	private String LastName;
	private String positionName;
	private String workPlace;
	private String gender;
	private String idPassportNumber;
	private String qqNumber;
	private String weChatNumber;
	private String telephone;

	public String getLaborContractId() {
		return laborContractId;
	}
	public void setLaborContractId(String laborContractId) {
		this.laborContractId = laborContractId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return LastName;
	}
	public void setLastName(String lastName) {
		LastName = lastName;
	}
	public String getPositionName() {
		return positionName;
	}
	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}
	public String getWorkPlace() {
		return workPlace;
	}
	public void setWorkPlace(String workPlace) {
		this.workPlace = workPlace;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getIdPassportNumber() {
		return idPassportNumber;
	}
	public void setIdPassportNumber(String idPassportNumber) {
		this.idPassportNumber = idPassportNumber;
	}
	public String getQqNumber() {
		return qqNumber;
	}
	public void setQqNumber(String qqNumber) {
		this.qqNumber = qqNumber;
	}
	public String getWeChatNumber() {
		return weChatNumber;
	}
	public void setWeChatNumber(String weChatNumber) {
		this.weChatNumber = weChatNumber;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	
	
}
